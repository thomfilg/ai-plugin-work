#!/usr/bin/env node
'use strict';

/**
 * codemod-plugin-root-preamble — insert the validated PLUGIN_ROOT preamble
 * into every script-calling SKILL.md (WP-10, design §G; probe P1/C18).
 *
 * Why validation instead of a `:-`/`:=` fallback: codex does NOT set
 * `CLAUDE_PLUGIN_ROOT` in the model's shell, and worse, a stale value from
 * the LAUNCHING shell can leak through **set-but-wrong** (probe P1 observed
 * `/home/<user>/.claude/plugins` with the marker read failing). A bare
 * default is therefore defeated; the preamble validates the env against the
 * skill's own SKILL.md marker (same-file check) before trusting it, and
 * self-locates from the skill's absolute path otherwise. Under Claude Code
 * the env var is always correct, so the preamble is a no-op there.
 *
 * Also appends the `$ARGUMENTS` note (design §G "Invocation guidance") to
 * skills whose body uses `$ARGUMENTS` — codex has no argument substitution
 * (GT §3.5); the argument is the text after the `$<skill>` mention.
 *
 * One-shot but IDEMPOTENT (marker-guarded) and checked in for auditability.
 *
 * Usage:
 *   node scripts/codemod-plugin-root-preamble.js [--check] [files...]
 *     default file set: every plugins/<plugin>/skills/**\/SKILL.md
 *     --check: exit 1 if any file would change (CI drift guard)
 */

const fs = require('node:fs');
const path = require('node:path');

const { REPO_ROOT, listSkillFiles } = require('./lib/skill-files');

const PREAMBLE_MARKER = '<!-- codex:plugin-root-preamble v1';
const ARGS_MARKER = '<!-- codex:arguments-note v1';

function preambleBlock(skillName) {
  return [
    `${PREAMBLE_MARKER} — generated by scripts/codemod-plugin-root-preamble.js; edit the codemod, then re-run -->`,
    '',
    '> **Resolve the plugin root before running any script below.** A codex shell',
    '> can inherit a stale, set-but-WRONG `CLAUDE_PLUGIN_ROOT` from its launcher,',
    '> so a bare `${CLAUDE_PLUGIN_ROOT:-…}` fallback is NOT safe — validate the',
    "> value against this skill's own SKILL.md first. Set `SKILL_MD` to this",
    "> file's absolute path (codex lists it next to this skill in your skills",
    '> list; under Claude Code the env var is always correct and the fallback',
    '> branch never runs):',
    '>',
    '> ```bash',
    '> SKILL_MD="/absolute/path/to/this/SKILL.md"   # from your skills list',
    `> if [ -f "\${CLAUDE_PLUGIN_ROOT:-/nonexistent}/skills/${skillName}/SKILL.md" ] &&`,
    `>    [ "\${CLAUDE_PLUGIN_ROOT}/skills/${skillName}/SKILL.md" -ef "$SKILL_MD" ]; then`,
    '>   PLUGIN_ROOT="$CLAUDE_PLUGIN_ROOT"                         # validated — use as-is',
    '> else',
    '>   PLUGIN_ROOT="$(cd "$(dirname "$SKILL_MD")/../.." && pwd)" # stale/unset — self-locate',
    '> fi',
    '> ```',
    '>',
    '> Wherever this skill writes `${CLAUDE_PLUGIN_ROOT}`, substitute the validated',
    '> `$PLUGIN_ROOT` value (shells do NOT share variables between tool calls —',
    '> re-resolve or inline the resolved path in each command).',
  ].join('\n');
}

function argumentsNoteBlock(skillName) {
  return [
    `${ARGS_MARKER} — generated by scripts/codemod-plugin-root-preamble.js -->`,
    '',
    '> **`$ARGUMENTS` under codex**: there is no argument substitution — the',
    `> argument (e.g. the ticket id) is the text after the \`$${skillName}\` skill`,
    "> mention in the user's message.",
  ].join('\n');
}

/** Index of the line AFTER the closing frontmatter fence, or -1. */
function frontmatterEnd(lines) {
  if (lines[0] !== '---') return -1;
  for (let i = 1; i < lines.length; i++) {
    if (lines[i] === '---') return i + 1;
  }
  return -1;
}

/**
 * Compute the rewritten content for one SKILL.md, or null when unchanged.
 */
function rewrite(file, raw) {
  const skillName = path.basename(path.dirname(file));
  const blocks = [];
  const needsRoot = raw.includes('CLAUDE_PLUGIN_ROOT') && !raw.includes(PREAMBLE_MARKER);
  const needsArgs = raw.includes('$ARGUMENTS') && !raw.includes(ARGS_MARKER);
  if (needsRoot) blocks.push(preambleBlock(skillName));
  if (needsArgs) blocks.push(argumentsNoteBlock(skillName));
  if (blocks.length === 0) return null;

  const lines = raw.split('\n');
  const at = frontmatterEnd(lines);
  if (at === -1) return null; // broken frontmatter — lint-skill-frontmatter owns that
  const head = lines.slice(0, at);
  const tail = lines.slice(at);
  // One blank line between the fence, each block, and the original body.
  const insert = blocks.flatMap((b) => ['', b]);
  return [...head, ...insert, ...tail].join('\n');
}

function processFile(file, check) {
  const raw = fs.readFileSync(file, 'utf8');
  const next = rewrite(file, raw);
  if (next === null || next === raw) return false;
  if (!check) fs.writeFileSync(file, next);
  return true;
}

function main() {
  const args = process.argv.slice(2);
  const check = args.includes('--check');
  const fileArgs = args.filter((a) => a !== '--check');
  const files = fileArgs.length > 0 ? fileArgs.map((f) => path.resolve(f)) : listSkillFiles();
  const changed = [];
  for (const file of files) {
    if (processFile(file, check)) changed.push(path.relative(REPO_ROOT, file));
  }
  const verb = check ? 'would change' : 'updated';
  for (const rel of changed) console.log(`${verb} ${rel}`);
  console.log(`codemod-plugin-root-preamble: ${changed.length} of ${files.length} file(s) ${verb}`);
  process.exit(check && changed.length > 0 ? 1 : 0);
}

if (require.main === module) main();

module.exports = { rewrite, preambleBlock, argumentsNoteBlock, PREAMBLE_MARKER, ARGS_MARKER };
